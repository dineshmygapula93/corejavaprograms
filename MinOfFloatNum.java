//Write a program to find the minimum of two floating point numbers by using the following method of java.lang.Float class.public static float min(float, float);
public class MinOfFloatNum {
    public static void main(String[] args) {
        Float f1=243.23f;
        Float f2=543.243f;
        Float rf=Float.min(f1,f2 );
        System.out.println("The min of the two numbers is "+rf);
    }   
}

