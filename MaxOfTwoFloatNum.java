// Write a program to find the maximum of two floating point numbers by using the following method of java.lang.Float class.public static float max(float, float);
public class MaxOfTwoFloatNum {
    public static void main(String[] args) {
        Float f1=243.23f;
        Float f2=543.243f;
        Float rf=Float.max(f1,f2 );
        System.out.println("The max of the two numbers is "+rf);
    }   
}
